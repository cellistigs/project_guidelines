## Utility module to implement arithmetic: 

def add(a,b):
    """add. A function to add together two values, a and b

    :param a: first number
    :type a: float or int
    :param b: second number
    :type b: float or int
    :return: the sum of a and b.
    :rtype: float or int
    """
    return a + b

def subtract(a,b):
    """subtract. A function that subtracts value b from a. 

    :param a: first number
    :type a: float or int
    :param b: second number
    :type b: float or int
    :return: the value a minus b.
    :rtype: float or int
    """
    return a - b
    
def multiply(a,b):
    """multiply. A function that multiplies the values a and b.

    :param a: first number
    :type a: float or int
    :param b: second number
    :type b: float or int
    :return: the value a times b.
    :rtype: float or int
    """
    return a*b
    
def divide(a,b):
    """divide. A function that divides the value of a by b.

    :param a: first number
    :type a: float or int
    :param b: second number
    :type b: float or int
    :return: the value a divided b.
    :rtype: float or int
    """
    return a/b
